class TestHookManagerInit:
    def test_init_no_config(self, git_project):
        hm = HookManager(project_root=git_project)
        assert hm.enabled is True
        assert hm.config == {}
        assert hm.project_root == git_project

    def test_init_with_config(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        assert hm.enabled is True
        assert "pre-commit" in hm.config
        assert hm.config["pre-commit"].commands == [
            "vibecollab check", "ruff check src/"
        ]

    def test_init_disabled(self, git_project):
        config = {"enabled": False, "rules": {}}
        hm = HookManager(project_root=git_project, config=config)
        assert hm.enabled is False

    def test_load_config_dict_format(self, git_project):
        """Test loading rules in dict format (commands + enabled)."""
        config = {
            "enabled": True,
            "rules": {
                "pre-commit": {
                    "commands": ["vibecollab check"],
                    "enabled": False,
                },
            },
        }
        hm = HookManager(project_root=git_project, config=config)
        assert hm.config["pre-commit"].commands == ["vibecollab check"]
        assert hm.config["pre-commit"].enabled is False


