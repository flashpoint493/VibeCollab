class TestInstall:
    def test_install_pre_commit(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        result = hm.install("pre-commit")
        assert result is True

        hook_path = git_project / ".git" / "hooks" / "pre-commit"
        assert hook_path.exists()
        content = hook_path.read_text(encoding="utf-8")
        assert "Generated by vibecollab" in content
        assert "pre-commit" in content

    def test_install_already_exists_no_force(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        result = hm.install("pre-commit", force=False)
        assert result is False

    def test_install_force_overwrite(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        result = hm.install("pre-commit", force=True)
        assert result is True

    def test_install_invalid_hook_type(self, git_project):
        hm = HookManager(project_root=git_project)
        result = hm.install("invalid-hook")
        assert result is False

    def test_install_not_git_repo(self, non_git_project):
        hm = HookManager(project_root=non_git_project)
        result = hm.install("pre-commit")
        assert result is False

    def test_install_uses_default_commands(self, git_project):
        """Install without config should use default commands."""
        hm = HookManager(project_root=git_project)
        hm.install("pre-commit")
        hook_path = git_project / ".git" / "hooks" / "pre-commit"
        content = hook_path.read_text(encoding="utf-8")
        assert "vibecollab check" in content

    def test_install_uses_config_commands(self, git_project, sample_config):
        """Install with config should use configured commands."""
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        hook_path = git_project / ".git" / "hooks" / "pre-commit"
        content = hook_path.read_text(encoding="utf-8")
        assert "ruff check" in content


