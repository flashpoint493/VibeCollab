class TestListHooks:
    def test_list_empty(self, git_project):
        hm = HookManager(project_root=git_project)
        assert hm.list_hooks() == []

    def test_list_installed(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        hm.install("pre-push")
        hooks = hm.list_hooks()
        assert "pre-commit" in hooks
        assert "pre-push" in hooks

    def test_list_not_git_repo(self, non_git_project):
        hm = HookManager(project_root=non_git_project)
        assert hm.list_hooks() == []

    def test_list_excludes_non_vibecollab(self, git_project, sample_config):
        """Custom hooks should not appear in list."""
        # Install vibecollab hook
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")

        # Add custom hook
        custom_hook = git_project / ".git" / "hooks" / "post-commit"
        custom_hook.write_text("#!/bin/bash\necho custom", encoding="utf-8")

        hooks = hm.list_hooks()
        assert "pre-commit" in hooks
        assert "post-commit" not in hooks


