class TestGenerateScript:
    def test_script_contains_commands(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        script = hm._generate_script("pre-commit", ["vibecollab check", "ruff check src/"])
        assert "Generated by vibecollab" in script
        assert "pre-commit" in script
        assert "vibecollab check" in script

    def test_script_platform_format(self, git_project):
        hm = HookManager(project_root=git_project)
        script = hm._generate_script("pre-commit", ["vibecollab check"])
        if os.name == "nt":
            assert "Write-Host" in script or "LASTEXITCODE" in script
        else:
            assert "#!/bin/bash" in script


