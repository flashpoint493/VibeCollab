class TestUninstall:
    def test_uninstall_vibecollab_hook(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        result = hm.uninstall("pre-commit")
        assert result is True
        assert not (git_project / ".git" / "hooks" / "pre-commit").exists()

    def test_uninstall_non_vibecollab_hook(self, git_project):
        """Should NOT remove hooks not generated by vibecollab."""
        hook_path = git_project / ".git" / "hooks" / "pre-commit"
        hook_path.write_text("#!/bin/bash\necho custom", encoding="utf-8")

        hm = HookManager(project_root=git_project)
        result = hm.uninstall("pre-commit")
        assert result is False
        assert hook_path.exists()

    def test_uninstall_nonexistent(self, git_project):
        hm = HookManager(project_root=git_project)
        result = hm.uninstall("pre-commit")
        assert result is False

    def test_uninstall_not_git_repo(self, non_git_project):
        hm = HookManager(project_root=non_git_project)
        result = hm.uninstall("pre-commit")
        assert result is False

    def test_uninstall_all(self, git_project, sample_config):
        hm = HookManager(project_root=git_project, config=sample_config)
        hm.install("pre-commit")
        hm.install("pre-push")
        count = hm.uninstall_all()
        assert count == 2
        assert not (git_project / ".git" / "hooks" / "pre-commit").exists()
        assert not (git_project / ".git" / "hooks" / "pre-push").exists()

    def test_uninstall_all_none_installed(self, git_project):
        hm = HookManager(project_root=git_project)
        count = hm.uninstall_all()
        assert count == 0


